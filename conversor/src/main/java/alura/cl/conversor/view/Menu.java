    private void BtnDolarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnDolarActionPerformed
       
        
        if ("Dolar".equals(BtnDolar.getText())) {
            JpanelDolar dolar = new JpanelDolar();        
            dolar.setSize(300, 300);
            dolar.setLocation(30, 50);

            Content.removeAll();
            Content.add(dolar, BorderLayout.CENTER);
            Content.revalidate();
            Content.repaint();
            
            
        }else{
            JpanelGramos gramos = new JpanelGramos();        
            gramos.setSize(300, 300);
            gramos.setLocation(30, 50);

            Content.removeAll();
            Content.add(gramos, BorderLayout.CENTER);
            Content.revalidate();
            Content.repaint();
        }
    }//GEN-LAST:event_BtnDolarActionPerformed

